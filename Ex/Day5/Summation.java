public class Summation{
    public static void main(String[] args){
        int n = 0;
        System.out.println(n);
        //This is where the loop starts, and i is at zero for consistency
        for(int i=20; i > 0; i--){
            n=n+i;
            System.out.println(n);
            } 
        System.out.println(n);
    
    
    
    
    
    
    }



}
