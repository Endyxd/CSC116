public class bmi{
	public static void main(String[] args){
			
			
			double height = (6*12)+2;
			int weight = 195;
			double bmi;
			bmi = (weight * 703)/(Math.pow(height,2));
			System.out.println("My weight is: " + weight + " pounds");
			System.out.println("My height is: " + height + " inches. (Or, 6feet, 2 inches)");
			System.out.println("My BMI is: " + bmi);
			System.out.println("My weight class is Overweight");
			
		
		
		
		
		
		
		
		
		
		
	}
}