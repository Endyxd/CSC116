public class NestedSummation{
    public static void main(String[] args){
        int answer = 0;
        for(int n = 1; n < 10; n++){
            for(int i = n; i >= 0; i--){
            answer+=i;
            
        }
        
        System.out.println(answer);
        answer = 0;
    
    
    
    
    }




}

}
